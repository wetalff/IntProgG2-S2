#Conversion de dolares a cordobas
dolares = float(input("Dime la cantidad de dolares que quieres convertir:"))
Tipo_de_cambio = float(input("Dime el tipo de cambio que quieres usar:"))
conversion_a_cordobas = dolares * Tipo_de_cambio
print("Los dolares mencionados convertidos a cordobas por el tipo de cambio de ", Tipo_de_cambio, " es de ", conversion_a_cordobas, " cordobas")
