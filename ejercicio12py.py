#Calcular el promedio de 5 asiganturas
nota_primer_asigantura = int(input("Dime la nota de la primera asignatura:"))
nota_segunda_asigantura = int(input("Dime la nota de la segunda asignatura:"))
nota_tercera_asigantura = int(input("Dime la nota de la tercera asignatura:"))
nota_cuarta_asigantura = int(input("Dime la nota de la cuarta asignatura:"))
nota_quinta_asigantura = int(input("Dime la nota de la quinta asignatura:"))
promedio = float(nota_primer_asigantura + nota_segunda_asigantura + nota_tercera_asigantura + nota_cuarta_asigantura + nota_quinta_asigantura) / 5
print("El promedio de las 5 asiganturas es de: ", promedio)

